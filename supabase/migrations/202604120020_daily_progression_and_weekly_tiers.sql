create table if not exists public.daily_scores (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users (id) on delete cascade,
  game_id text not null,
  date_key text not null,
  seed integer not null,
  score integer not null default 0 check (score >= 0),
  replay_code text,
  summary_json jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (user_id, game_id, date_key)
);

create index if not exists daily_scores_game_date_score_idx
  on public.daily_scores (game_id, date_key, score desc, created_at asc);

create index if not exists daily_scores_user_game_date_idx
  on public.daily_scores (user_id, game_id, date_key);

alter table public.daily_scores enable row level security;

