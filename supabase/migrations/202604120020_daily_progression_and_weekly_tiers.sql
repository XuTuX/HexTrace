create or replace function public.get_my_weekly_season_summary(p_game_id text)
returns table (
  week_key text,
  participant_count integer,
  rank integer,
  score integer,
  tier text
)
language sql
stable
security definer
set search_path = public
as $$
  with current_week as (
    select to_char(timezone('Asia/Seoul', now()), 'IYYY-IW') as week_key
  ),
  ranked as (
    select
      ws.user_id,
      ws.score,
      rank() over (order by ws.score desc) as rank,
      count(*) over () as participant_count
    from public.weekly_scores ws
    cross join current_week cw
    where ws.game_id = p_game_id
      and ws.week_key = cw.week_key
  )
  select
    cw.week_key,
    r.participant_count::integer,
    r.rank::integer,
    r.score,
    case
      when r.participant_count <= 0 then 'bronze'
      when (r.rank::numeric / r.participant_count::numeric) <= 0.01 then 'diamond'
      when (r.rank::numeric / r.participant_count::numeric) <= 0.05 then 'platinum'
      when (r.rank::numeric / r.participant_count::numeric) <= 0.20 then 'gold'
      when (r.rank::numeric / r.participant_count::numeric) <= 0.50 then 'silver'
      else 'bronze'
    end as tier
  from current_week cw
  left join ranked r
    on r.user_id = auth.uid();
$$;

