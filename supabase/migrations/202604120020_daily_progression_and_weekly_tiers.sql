create or replace function public.get_daily_challenge_seed(
  p_game_id text,
  p_date_key text
)
returns integer
language sql
immutable
as $$
  select (
    (('x' || substr(md5(coalesce(p_game_id, '') || ':' || coalesce(p_date_key, '')), 1, 7))::bit(28)::int)
    % 1000000
  )::integer;
$$;

create or replace function public.get_daily_challenge(p_game_id text)
returns table (
  date_key text,
  seed integer,
  has_used_official_attempt boolean,
  my_score integer
)
language sql
stable
security definer
set search_path = public
as $$
  with current_day as (
    select to_char(timezone('Asia/Seoul', now()), 'YYYY-MM-DD') as date_key
  ),
  my_entry as (
    select ds.score
    from public.daily_scores ds
    cross join current_day cd
    where ds.user_id = auth.uid()
      and ds.game_id = p_game_id
      and ds.date_key = cd.date_key
    limit 1
  )
  select
    cd.date_key,
    public.get_daily_challenge_seed(p_game_id, cd.date_key) as seed,
    exists(select 1 from my_entry) as has_used_official_attempt,
    (select score from my_entry limit 1) as my_score
  from current_day cd;
$$;

create or replace function public.submit_daily_score(
  p_game_id text,
  p_date_key text,
  p_seed integer,
  p_score integer,
  p_replay_code text default null,
  p_summary_json text default null
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_current_date_key text := to_char(timezone('Asia/Seoul', now()), 'YYYY-MM-DD');
  v_expected_seed integer := public.get_daily_challenge_seed(p_game_id, p_date_key);
  v_inserted_score integer;
begin
  if v_user_id is null then
    raise exception 'Not authenticated';
  end if;

  if p_game_id is null or btrim(p_game_id) = '' then
    raise exception 'game_id is required';
  end if;

  if p_date_key is null or btrim(p_date_key) = '' then
    raise exception 'date_key is required';
  end if;

  if p_date_key <> v_current_date_key then
    raise exception 'Daily challenge is only valid for today';
  end if;

  if p_seed is distinct from v_expected_seed then
    raise exception 'Invalid daily challenge seed';
  end if;

  insert into public.daily_scores (
    user_id,
    game_id,
    date_key,
    seed,
    score,
    replay_code,
    summary_json
  )
  values (
    v_user_id,
    p_game_id,
    p_date_key,
    p_seed,
    greatest(coalesce(p_score, 0), 0),
    p_replay_code,
    case
      when p_summary_json is null or btrim(p_summary_json) = '' then null
      else p_summary_json::jsonb
    end
  )
  on conflict (user_id, game_id, date_key) do nothing
  returning score into v_inserted_score;

  if v_inserted_score is null then
    raise exception 'Daily attempt already used';
  end if;

  return v_inserted_score;
end;
$$;

create or replace function public.get_daily_leaderboard(
  p_game_id text,
  p_date_key text default null,
  p_limit integer default 20
)
returns table (
  user_id uuid,
  score integer,
  nickname text,
  avatar_url text,
  rank bigint
)
language sql
stable
security definer
set search_path = public
as $$
  with target_day as (
    select coalesce(p_date_key, to_char(timezone('Asia/Seoul', now()), 'YYYY-MM-DD')) as date_key
  ),
  ranked as (
    select
      ds.user_id,
      ds.score,
      ds.created_at,
      rank() over (order by ds.score desc) as rank
    from public.daily_scores ds
    cross join target_day td
    where ds.game_id = p_game_id
      and ds.date_key = td.date_key
  )
  select
    r.user_id,
    r.score,
    coalesce(
      nullif(btrim(p.nickname), ''),
      'Player ' || upper(substr(r.user_id::text, 1, 6))
    ) as nickname,
    p.avatar_url,
    r.rank
  from ranked r
  left join public.profiles p
    on p.id = r.user_id
  order by r.rank asc, r.created_at asc, r.user_id asc
  limit greatest(coalesce(p_limit, 20), 1);
$$;

